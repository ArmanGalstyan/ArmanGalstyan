     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $lessons = Lesson::with(['group'=>function($m){
            $m->with('user');
        }])->orderBy('id','desc')->get();
        $groups = Group::all()->where('user_id', Auth::id());
        return view('lesson')->with('lessons',$lessons)->with('groups',$groups);
    }

